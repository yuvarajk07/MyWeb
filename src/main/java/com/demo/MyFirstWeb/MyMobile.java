package com.demo.MyFirstWeb;

public class MyMobile {
	
	private String brand;
	private int price;
	private String color;
	private int size;
	private boolean isWarrenty;
	private boolean istouchable;
	
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}

	public boolean getisWarrenty() {
		return isWarrenty;
	}
	public void setWarrenty(boolean isWarrenty) {
		this.isWarrenty = isWarrenty;
	}
	public boolean isIstouchable() {
		return istouchable;
	}
	public void setIstouchable(boolean istouchable) {
		this.istouchable = istouchable;
	}
	

}
